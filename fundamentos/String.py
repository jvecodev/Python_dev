movieName = "Top Gun"

print(movieName.upper())  # Deixa tudo maiusculo
print(movieName.lower())  # Deixa tudo minusculo
print(movieName.title())  # Deixa a primeira letra de cada palavra maiuscula
print(movieName.capitalize())  # Deixa a primeira letra da string maiuscula
print(movieName.strip())  # Remove espacos no inicio e no fim
print(movieName.replace("o", "0"))  # Substitui caracteres
print(movieName.split())  # Divide a string em uma lista
print(" ".join(movieName))  # Junta os caracteres da string com o caractere especificado
print(movieName.count("Top"))  